    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed
        int pos = TablaReservas.getSelectedRow();
        int conv = TablaReservas.convertRowIndexToModel(pos);
        if(pos != -1){
            DefaultTableModel tabla = (DefaultTableModel) TablaReservas.getModel();
            int id = Integer.parseInt(TablaReservas.getModel().getValueAt(conv,0).toString());
            Main.shop.eliminarReserva(id);
            actualizarTabla();
        }
    }//GEN-LAST:event_btnBorrarActionPerformed

