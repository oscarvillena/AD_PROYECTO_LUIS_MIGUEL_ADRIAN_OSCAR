    public void formatearTabla(){
        DefaultTableModel tabla = new DefaultTableModel();
        tabla.setColumnIdentifiers(new String[] {"id", "Tipo", "Usuario" ,"Fecha alta", "Fecha baja"});
        TablaReservas.setModel(tabla);
        sorter = new TableRowSorter(tabla);
        
    }
    
    public void actualizarTabla() {
        SwingUtilities.invokeLater(() -> { // es una expreion landa que si no se usa se actualiza en un hilo en el que falla
            DefaultTableModel tabla = (DefaultTableModel) TablaReservas.getModel();
            tabla.setRowCount(0);
            if(PantallaPrincipal.getUser().getAdmin()){
                for (Reserva res : Main.shop.devolverTodasReservas()) {
                    tabla.addRow(res.toArrayString());
                }
            }else{
                for (Reserva res : Main.shop.devolverReservas(PantallaPrincipal.getUser())) {
                    tabla.addRow(res.toArrayString());
                }
            }
        });
        TablaReservas.setRowSorter(sorter);
        List <RowSorter.SortKey> sorkeys = new ArrayList();
        sorkeys.add(new RowSorter.SortKey(2, SortOrder.ASCENDING));
        sorter.setSortKeys(sorkeys);
    }
